import UIKit

class Calculations: NSObject {

    
    public func addNumbers(num1 : Int, num2 : Int) -> Int {
        return num1 + num2
    }
    
    public func multiplyNumbers(num1 : Int, num2 : Int) -> Int {
        return num1 * num2
    }
}
